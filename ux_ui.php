                    <div class="faq-question">
                        <span>What's the difference between UX and UI design?</span>
                        <span>+</span>
                    </div>
                    <div class="faq-answer">
                        <p>UX (User Experience) design focuses on the overall feel of the product and how users interact with it. It involves research, information architecture, user flows, wireframing, and prototyping to create intuitive, meaningful experiences. UI (User Interface) design is more concerned with the visual elements and interactivity of a product, including color schemes, typography, buttons, icons, and other interface elements. While distinct, UX and UI are complementary disciplines that work together to create effective digital products.</p>
                    </div>
                </div>
                <div class="faq-item">
                    <div class="faq-question">
                        <span>How long does a UX/UI design project typically take?</span>
                        <span>+</span>
                    </div>
                    <div class="faq-answer">
                        <p>The timeline for UX/UI design projects varies depending on the scope, complexity, and type of project. A simple website redesign might take 4-6 weeks, while a complex application design could take 2-3 months or more. Our process includes research, wireframing, prototyping, UI design, and testing, each requiring adequate time to ensure quality results. During our initial consultation, we'll provide a more accurate timeline based on your specific requirements.</p>
                    </div>
                </div>
